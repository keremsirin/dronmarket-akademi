document.getElementById('city').addEventListener('change', function () {
  var index = data.findIndex(
    (obj) => obj.city == select.options[select.selectedIndex].text
  )
  var select2 = document.getElementById('town')
  select2.options.length = 0
  for (var i = 0; i < data[index].town.length; i++) {
    var option = document.createElement('option')
    option.value = option.text = data[index].town[i]
    select2.add(option)
  }
})
